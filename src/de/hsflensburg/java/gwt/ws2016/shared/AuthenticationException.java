package de.hsflensburg.java.gwt.ws2016.shared;

public class AuthenticationException extends Exception
{
	private static final long serialVersionUID = 1L;

	public AuthenticationException()
	{
	}

	public AuthenticationException(String rArg0)
	{
		super(rArg0);
	}

	public AuthenticationException(String rArg0, Throwable rArg1)
	{
		super(rArg0, rArg1);
	}

	public AuthenticationException(Throwable rArg0)
	{
		super(rArg0);
	}
}
